// Helper function to get contracts for current network
export function getContracts(chainId: number) {
  switch (chainId) {
    case 11155111: // Sepolia
      return CONTRACTS.sepolia;
    case 31337: // Hardhat local
    case 1337: // Localhost
      return CONTRACTS.localhost;
    default:
      console.warn(`Unknown chain ID: ${chainId}, using localhost contracts`);
      return CONTRACTS.localhost;
  }
}

